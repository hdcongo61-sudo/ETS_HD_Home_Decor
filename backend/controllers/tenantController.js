// ── Super-admin: platform overview stats (revenue, funnel, attention) ──
// GET /api/tenants/stats/overview
const getOverviewStats = asyncHandler(async (req, res) => {
  const tenants = await Tenant.find({}).lean();
  const now = new Date();
  const startOfMonth = new Date(now.getFullYear(), now.getMonth(), 1);

  const paying = tenants.filter((t) => t.status === 'active');
  const trials = tenants.filter((t) => t.status === 'trial' && !isExpired(t));
  const suspended = tenants.filter((t) => t.status === 'suspended');
  const churned = tenants.filter((t) => t.status === 'expired' || (t.status === 'trial' && isExpired(t)));

  // MRR = sum of monthlyPrice for active (paying) tenants
  const mrr = paying.reduce((sum, t) => sum + (t.monthlyPrice || 0), 0);

  // Revenue collected this month (from payment records)
  let revenueThisMonth = 0;
  tenants.forEach((t) => {
    (t.payments || []).forEach((p) => {
      if (p.paidAt && new Date(p.paidAt) >= startOfMonth) revenueThisMonth += p.amount || 0;
    });
  });

  // Signups over the last 6 months
  const signups = {};
  for (let i = 5; i >= 0; i -= 1) {
    const d = new Date(now.getFullYear(), now.getMonth() - i, 1);
    signups[`${d.getFullYear()}-${String(d.getMonth() + 1).padStart(2, '0')}`] = 0;
  }
  tenants.forEach((t) => {
    const d = new Date(t.createdAt);
    const key = `${d.getFullYear()}-${String(d.getMonth() + 1).padStart(2, '0')}`;
    if (key in signups) signups[key] += 1;
  });

  // Plan distribution
  const planDist = {};
  tenants.forEach((t) => { planDist[t.plan] = (planDist[t.plan] || 0) + 1; });

  // Growth & churn over the last 6 months: new shops (createdAt) vs lost shops
  // (suspend events from the audit log). Reactivations net the loss back.
  const growth = {};
  for (let i = 5; i >= 0; i -= 1) {
    const d = new Date(now.getFullYear(), now.getMonth() - i, 1);
    growth[`${d.getFullYear()}-${String(d.getMonth() + 1).padStart(2, '0')}`] = { new: 0, lost: 0 };
  }
  Object.keys(signups).forEach((key) => { if (growth[key]) growth[key].new = signups[key]; });
  const sixMonthsAgo = new Date(now.getFullYear(), now.getMonth() - 5, 1);
  const churnEvents = await PlatformAudit.find({
    action: { $in: ['tenant.suspend', 'tenant.reactivate'] },
    createdAt: { $gte: sixMonthsAgo },
  }).select('action createdAt').lean();
  churnEvents.forEach((ev) => {
    const d = new Date(ev.createdAt);
    const key = `${d.getFullYear()}-${String(d.getMonth() + 1).padStart(2, '0')}`;
    if (!growth[key]) return;
    if (ev.action === 'tenant.suspend') growth[key].lost += 1;
    else if (ev.action === 'tenant.reactivate') growth[key].lost = Math.max(0, growth[key].lost - 1);
  });
  // Current-month churn rate = lost this month / shops active at month start.
  const monthKeys = Object.keys(growth);
  const lastKey = monthKeys[monthKeys.length - 1];
  const lostThisMonth = growth[lastKey]?.lost || 0;
  const activeBase = paying.length + lostThisMonth;
  const churnRate = activeBase > 0 ? Math.round((lostThisMonth / activeBase) * 1000) / 10 : 0;

  // Attention lists
  const liveStats = await getLiveStats(tenants.map((t) => t._id));
  const soon = 3 * 86400000;
  const inactiveCutoff = new Date(now - 14 * 86400000);

  const trialsExpiring = trials
    .filter((t) => t.trialEndsAt && new Date(t.trialEndsAt) - now <= soon)
    .map((t) => ({ _id: t._id, name: t.name, trialEndsAt: t.trialEndsAt }));

  const paymentsOverdue = tenants
    .filter((t) => t.status === 'active' && t.nextPaymentDue && new Date(t.nextPaymentDue) < now)
    .map((t) => ({ _id: t._id, name: t.name, nextPaymentDue: t.nextPaymentDue, amount: t.monthlyPrice }));

  const dormant = tenants
    .filter((t) => t.status !== 'suspended' && (!t.stats?.lastActiveAt || new Date(t.stats.lastActiveAt) < inactiveCutoff))
    .map((t) => ({ _id: t._id, name: t.name, lastActiveAt: t.stats?.lastActiveAt || null }));

  const nearLimit = tenants
    .map((t) => {
      const s = liveStats[t._id.toString()] || {};
      const userPct = t.maxUsers ? (s.userCount || 0) / t.maxUsers : 0;
      const prodPct = t.maxProducts ? (s.productCount || 0) / t.maxProducts : 0;
      const pct = Math.max(userPct, prodPct);
      return { _id: t._id, name: t.name, pct, userCount: s.userCount || 0, maxUsers: t.maxUsers, productCount: s.productCount || 0, maxProducts: t.maxProducts };
    })
    .filter((t) => t.pct >= 0.8);

  res.json({
    revenue: { mrr, arr: mrr * 12, revenueThisMonth },
    funnel: {
      total: tenants.length,
      paying: paying.length,
      trials: trials.length,
      suspended: suspended.length,
      churned: churned.length,
    },
    signups,
    planDist,
    growth,
    churnRate,
    attention: { trialsExpiring, paymentsOverdue, dormant, nearLimit },
  });
});

// ── Super-admin: record a payment for a tenant ──
// POST /api/tenants/:id/payment
const recordPayment = asyncHandler(async (req, res) => {
  const { amount, method, period, note, nextPaymentDue } = req.body;
  const numericAmount = Number(amount);
  if (!Number.isFinite(numericAmount) || numericAmount <= 0) {
    res.status(400);
    throw new Error('Montant invalide.');
  }

  const tenant = await Tenant.findById(req.params.id);
  if (!tenant) {
    res.status(404);
    throw new Error('Boutique introuvable.');
  }

  const paidAt = new Date();
  tenant.payments.push({
    amount: numericAmount,
    method: method || 'cash',
    period: period || `${paidAt.getFullYear()}-${String(paidAt.getMonth() + 1).padStart(2, '0')}`,
    note: note || '',
    recordedBy: req.user._id,
    paidAt,
  });
  tenant.lastPaymentAt = paidAt;
  if (nextPaymentDue) tenant.nextPaymentDue = new Date(nextPaymentDue);
  // A payment on a trial/suspended shop activates it.
  if (tenant.status !== 'active') tenant.status = 'active';

  await tenant.save();

  PlatformAudit.record({ req, action: 'tenant.payment', tenant, meta: { amount: numericAmount, method: method || 'cash', period } });

  res.status(201).json(tenant);
});

