// ── Super-admin: update tenant status / plan ──
// PUT /api/tenants/:id
const updateTenant = asyncHandler(async (req, res) => {
  const { status, plan, maxUsers, maxProducts, subscriptionEndsAt, billingNotes, branding } = req.body;

  const tenant = await Tenant.findById(req.params.id);
  if (!tenant) {
    res.status(404);
    throw new Error('Boutique introuvable.');
  }

  const before = { status: tenant.status, plan: tenant.plan };

  if (status) tenant.status = status;

  // Changing the plan cascades the catalog price + limits (still overridable below).
  if (plan && plan !== tenant.plan) {
    tenant.plan = plan;
    const cat = await resolvePlan(plan);
    if (cat) {
      tenant.monthlyPrice = cat.price;
      tenant.maxUsers = cat.maxUsers;
      tenant.maxProducts = cat.maxProducts;
    }
  }

  if (maxUsers !== undefined) tenant.maxUsers = Number(maxUsers);
  if (maxProducts !== undefined) tenant.maxProducts = Number(maxProducts);
  if (req.body.monthlyPrice !== undefined) tenant.monthlyPrice = Number(req.body.monthlyPrice) || 0;
  if (subscriptionEndsAt) tenant.subscriptionEndsAt = new Date(subscriptionEndsAt);
  if (req.body.nextPaymentDue) tenant.nextPaymentDue = new Date(req.body.nextPaymentDue);
  if (req.body.trialEndsAt) tenant.trialEndsAt = new Date(req.body.trialEndsAt);
  if (billingNotes !== undefined) tenant.billingNotes = billingNotes;
  if (req.body.dialCode !== undefined) {
    // Keep only digits and a single leading "+", normalised to "+<digits>".
    const raw = String(req.body.dialCode).replace(/[^\d+]/g, '').replace(/(?!^)\+/g, '');
    tenant.dialCode = raw ? (raw.startsWith('+') ? raw : `+${raw.replace(/^0+/, '')}`) : '';
  }
  if (branding) tenant.branding = { ...tenant.branding, ...branding };

  // Per-shop feature overrides — keep only known features with boolean values.
  if (req.body.featureOverrides && typeof req.body.featureOverrides === 'object') {
    const clean = {};
    ALL_FEATURES.forEach((key) => {
      const v = req.body.featureOverrides[key];
      if (v === true || v === false) clean[key] = v;
    });
    tenant.featureOverrides = clean;
    tenant.markModified('featureOverrides');
  }

  await tenant.save();

  // Audit the meaningful state changes
  if (status && status !== before.status) {
    const action = status === 'suspended' ? 'tenant.suspend'
      : (before.status === 'suspended' ? 'tenant.reactivate' : 'tenant.update');
    PlatformAudit.record({ req, action, tenant, meta: { from: before.status, to: status, reason: req.body.reason || '' } });
  }
  if (plan && plan !== before.plan) {
    PlatformAudit.record({ req, action: 'tenant.plan_change', tenant, meta: { from: before.plan, to: plan } });
  }

  res.json(tenant);
});

