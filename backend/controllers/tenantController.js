// Resolve a single plan's {label, price, maxUsers, maxProducts} from the
// editable DB catalog, falling back to the hardcoded defaults.
const resolvePlan = async (planKey) => {
  try {
    const cfg = await PlatformConfig.getCatalog();
    const plan = cfg.plans?.[planKey];
    if (plan) return plan.toObject ? plan.toObject() : plan;
  } catch { /* fall through */ }
  return PLAN_CATALOG[planKey] || PLAN_CATALOG.trial;
};

// Helper: compute live counts for a list of tenantIds
const getLiveStats = async (tenantIds) => {
  if (!tenantIds.length) return {};

  const [users, products, sales] = await Promise.all([
    User.aggregate([
      { $match: { tenantId: { $in: tenantIds } } },
      { $group: { _id: '$tenantId', count: { $sum: 1 } } },
    ]),
    mongoose.modelNames().includes('Product')
      ? mongoose.model('Product').aggregate([
          { $match: { tenantId: { $in: tenantIds } } },
          { $group: { _id: '$tenantId', count: { $sum: 1 } } },
        ])
      : Promise.resolve([]),
    mongoose.modelNames().includes('Sale')
      ? mongoose.model('Sale').aggregate([
          { $match: { tenantId: { $in: tenantIds } } },
          { $group: { _id: '$tenantId', count: { $sum: 1 } } },
        ])
      : Promise.resolve([]),
  ]);

  const toMap = (arr) => Object.fromEntries(arr.map((r) => [r._id.toString(), r.count]));
  const uMap = toMap(users);
  const pMap = toMap(products);
  const sMap = toMap(sales);

  const result = {};
  tenantIds.forEach((id) => {
    const k = id.toString();
    result[k] = { userCount: uMap[k] || 0, productCount: pMap[k] || 0, saleCount: sMap[k] || 0 };
  });
  return result;
};

const isExpired = (t) =>
  t.status === 'expired' ||
  (t.status === 'trial' && t.trialEndsAt && new Date() > new Date(t.trialEndsAt));

// ── Public: Register a new tenant (shop owner signup) ──
// POST /api/tenants/register
const registerTenant = asyncHandler(async (req, res) => {
  const { shopName, ownerName, ownerEmail, ownerPhone, password } = req.body;

  if (!shopName || !ownerName || !ownerEmail || !password) {
    res.status(400);
    throw new Error('Nom de la boutique, nom du propriétaire, email et mot de passe sont requis.');
  }

  if (password.length < 6) {
    res.status(400);
    throw new Error('Le mot de passe doit contenir au moins 6 caractères.');
  }

  // Check email not already used as tenant owner
  const existingTenant = await Tenant.findOne({ ownerEmail: ownerEmail.toLowerCase() });
  if (existingTenant) {
    res.status(409);
    throw new Error('Un compte existe déjà avec cet email.');
  }

  // Create tenant first (slug + code auto-generated by pre-save hook)
  const trialPlan = await resolvePlan('trial');
  const tenant = await Tenant.create({
    name: shopName.trim(),
    ownerName: ownerName.trim(),
    ownerEmail: ownerEmail.toLowerCase().trim(),
    ownerPhone: ownerPhone ? ownerPhone.trim() : '',
    monthlyPrice: trialPlan.price || 0,
    maxUsers: trialPlan.maxUsers,
    maxProducts: trialPlan.maxProducts,
    branding: { appName: shopName.trim() },
  });

  // Create the admin user for this tenant
  const adminUser = await User.create({
    tenantId: tenant._id,
    name: ownerName.trim(),
    email: ownerEmail.toLowerCase().trim(),
    phone: ownerPhone ? ownerPhone.trim() : '',
    password,
    isAdmin: true,
    isActive: true,
  });

  // Seed this shop's Settings with the info entered at creation
  await seedTenantSettings({
    tenantId: tenant._id,
    shopName: tenant.name,
    ownerEmail: tenant.ownerEmail,
    ownerPhone: tenant.ownerPhone,
    primaryColor: tenant.branding?.primaryColor,
  });

  // Update tenant stats
  await Tenant.findByIdAndUpdate(tenant._id, { 'stats.userCount': 1 });

  const token = generateToken(adminUser._id, tenant._id);

  res.status(201).json({
    token,
    tenant: {
      _id: tenant._id,
      name: tenant.name,
      slug: tenant.slug,
      code: tenant.code,
      plan: tenant.plan,
      status: tenant.status,
      trialEndsAt: tenant.trialEndsAt,
      branding: tenant.branding,
    },
    user: {
      _id: adminUser._id,
      name: adminUser.name,
      email: adminUser.email,
      isAdmin: true,
    },
  });
});

// ── Super-admin: list all tenants ──
// GET /api/tenants
const getTenants = asyncHandler(async (req, res) => {
  // Optional filters: ?status=trial&plan=basic&search=omar
  const filter = {};
  if (req.query.status) filter.status = req.query.status;
  if (req.query.plan)   filter.plan   = req.query.plan;
  if (req.query.search) {
    const re = new RegExp(req.query.search.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'i');
    filter.$or = [{ name: re }, { ownerEmail: re }, { ownerName: re }, { code: re }];
  }

  const tenants = await Tenant.find(filter).sort({ createdAt: -1 }).lean();

  // Live counts for all returned tenants
  const statsMap = await getLiveStats(tenants.map((t) => t._id));

  res.json(
    tenants.map((t) => {
      const live = statsMap[t._id.toString()] || {};
      return {
        ...t,
        stats: { ...t.stats, ...live },
        isActive: t.status !== 'suspended' && !isExpired(t),
      };
    })
  );
});

