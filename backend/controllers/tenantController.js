// ── Super-admin: impersonate a tenant's admin user ──
// POST /api/tenants/:id/impersonate
// Returns a short-lived token scoped to that tenant so the super-admin
// can log in as the shop owner for debugging / support.
const impersonateTenant = asyncHandler(async (req, res) => {
  const tenant = await Tenant.findById(req.params.id);
  if (!tenant) {
    res.status(404);
    throw new Error('Boutique introuvable.');
  }

  // Find the primary admin of this tenant
  const adminUser = await User.findOne({ tenantId: tenant._id, isAdmin: true, isActive: true })
    .sort({ createdAt: 1 })
    .lean();

  if (!adminUser) {
    res.status(404);
    throw new Error('Aucun administrateur actif trouvé pour cette boutique.');
  }

  // Issue a token valid 2 hours — shorter than normal 30d
  const jwt = require('jsonwebtoken');
  const token = jwt.sign(
    { id: adminUser._id, tenantId: tenant._id.toString(), impersonatedBy: req.user._id.toString() },
    process.env.JWT_SECRET,
    { expiresIn: '2h' }
  );

  PlatformAudit.record({ req, action: 'tenant.impersonate', tenant, meta: { asUser: adminUser.email } });

  res.json({
    token,
    tenant: { _id: tenant._id, name: tenant.name, code: tenant.code },
    user:   { _id: adminUser._id, name: adminUser.name, email: adminUser.email, isAdmin: true },
    expiresIn: '2h',
  });
});

