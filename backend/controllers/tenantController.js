// ── Super-admin: update the plan catalog (prices + limits) ──
// PUT /api/tenants/plans
const updatePlans = asyncHandler(async (req, res) => {
  const cfg = await PlatformConfig.getCatalog();
  const incoming = req.body.plans || req.body;

  ['trial', 'basic', 'pro', 'enterprise'].forEach((key) => {
    const p = incoming[key];
    if (!p) return;
    if (p.label !== undefined)       cfg.plans[key].label = String(p.label);
    if (p.price !== undefined)       cfg.plans[key].price = Math.max(0, Number(p.price) || 0);
    if (p.maxUsers !== undefined)    cfg.plans[key].maxUsers = Math.max(1, Number(p.maxUsers) || 1);
    if (p.maxProducts !== undefined) cfg.plans[key].maxProducts = Math.max(1, Number(p.maxProducts) || 1);
    if (Array.isArray(p.features))   cfg.plans[key].features = p.features.map(String);
  });

  await cfg.save();
  invalidateFeatureCache();

  PlatformAudit.record({ req, action: 'plans.update', tenant: null, meta: {} });

  res.json(cfg.plans);
});

